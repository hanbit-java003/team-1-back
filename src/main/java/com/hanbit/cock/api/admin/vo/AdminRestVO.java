package com.hanbit.cock.api.admin.vo;

public class AdminRestVO {
	
	private int rid;
	private String name;
	private int count;
	
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}	

}
