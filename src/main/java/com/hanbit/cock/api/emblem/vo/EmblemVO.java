package com.hanbit.cock.api.emblem.vo;

public class EmblemVO {
	private String name;
	private String img;
	private String howAchieve;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getHowAchieve() {
		return howAchieve;
	}
	public void setHowAchieve(String howAchieve) {
		this.howAchieve = howAchieve;
	}
}
